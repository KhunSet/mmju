package com.mmju.hhk.ep4.freeq;

public class FreqChar {
	
	private Character c;
	private int count;
	
	public FreqChar(Character c, int count) {
		this.c = c;
		this.count = count;
	}

	public Character getC() {
		return c;
	}

	public void setC(Character c) {
		this.c = c;
	}

	public int getCount() {
		return count;
	}

	public void setCount(int count) {
		this.count = count;
	}

}
